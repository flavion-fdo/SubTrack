const { initDatabase, dbRun, dbAll, db } = require('../config/db');
const { checkRenewals } = require('../services/alertEngine');
const fs = require('fs');
const path = require('path');

const logFilePath = path.resolve(__dirname, '../../../alerts.log');

const getFutureDateString = (daysAhead) => {
  const targetDate = new Date();
  targetDate.setDate(targetDate.getDate() + daysAhead);
  const year = targetDate.getFullYear();
  const month = String(targetDate.getMonth() + 1).padStart(2, '0');
  const day = String(targetDate.getDate()).padStart(2, '0');
  return `${year}-${month}-${day}`;
};

const runTest = async () => {
  console.log('--- STARTING ALERT ENGINE VERIFICATION TEST ---');

  try {
    // 1. Initialise the database tables
    await initDatabase();

    // 2. Clean previous test data
    console.log('Cleaning existing test records...');
    await dbRun('DELETE FROM alert_history');
    await dbRun('DELETE FROM subscriptions');
    await dbRun('DELETE FROM users');

    // Remove old alerts log file if it exists
    if (fs.existsSync(logFilePath)) {
      fs.unlinkSync(logFilePath);
      console.log('Cleared existing alerts.log file.');
    }

    // 3. Create a test user
    console.log('Creating test user...');
    const userResult = await dbRun(
      'INSERT INTO users (email, password) VALUES (?, ?)',
      ['tester@example.com', 'hashedpassword123']
    );
    const userId = userResult.id;
    console.log(`Test user created with ID: ${userId}`);

    // Dates for test subscriptions
    const dateIn3Days = getFutureDateString(3);
    const dateIn2Days = getFutureDateString(2);

    console.log(`Setting up test subscriptions...`);
    console.log(`Date in 3 days: ${dateIn3Days}`);
    console.log(`Date in 2 days: ${dateIn2Days}`);

    // Sub 1: Renewing in exactly 3 days. SHOULD alert.
    const sub1 = await dbRun(
      `INSERT INTO subscriptions (user_id, service_name, cost, billing_cycle, next_renewal_date, category)
       VALUES (?, ?, ?, ?, ?, ?)`,
      [userId, 'Netflix (Active - Should Alert)', 15.99, 'monthly', dateIn3Days, 'Entertainment']
    );

    // Sub 2: Renewing in 2 days. Should NOT alert.
    const sub2 = await dbRun(
      `INSERT INTO subscriptions (user_id, service_name, cost, billing_cycle, next_renewal_date, category)
       VALUES (?, ?, ?, ?, ?, ?)`,
      [userId, 'Spotify (Active - Should NOT Alert)', 9.99, 'monthly', dateIn2Days, 'Entertainment']
    );

    // Sub 3: Renewing in 3 days, but has already alerted. Should NOT alert.
    const sub3 = await dbRun(
      `INSERT INTO subscriptions (user_id, service_name, cost, billing_cycle, next_renewal_date, category)
       VALUES (?, ?, ?, ?, ?, ?)`,
      [userId, 'Adobe CC (Already Alerted - Should NOT Alert)', 54.99, 'monthly', dateIn3Days, 'Software']
    );
    // Seed alert history for Sub 3
    await dbRun(
      'INSERT INTO alert_history (subscription_id, renewal_date) VALUES (?, ?)',
      [sub3.id, dateIn3Days]
    );

    console.log('Test database seeded successfully.');

    // 4. Execute the Alert Engine check
    console.log('\nExecuting checkRenewals()...');
    await checkRenewals();
    console.log('checkRenewals() execution finished.\n');

    // 5. Verify the results
    console.log('--- VERIFYING RESULTS ---');
    
    // Check database alert history table
    const histories = await dbAll('SELECT * FROM alert_history');
    console.log(`Alert history count in database: ${histories.length} (Expected: 2)`);
    // Expected 2: the pre-seeded one for Adobe, plus the new one for Netflix.
    
    // Read the logs generated by emailService.js
    if (fs.existsSync(logFilePath)) {
      const logs = fs.readFileSync(logFilePath, 'utf8');
      console.log('--- CONTENT OF alerts.log ---');
      console.log(logs);
      console.log('------------------------------');

      const containsNetflix = logs.includes('Netflix (Active - Should Alert)');
      const containsSpotify = logs.includes('Spotify (Active - Should NOT Alert)');
      const containsAdobe = logs.includes('Adobe CC (Already Alerted - Should NOT Alert)');

      console.log(`Netflix alert logged? ${containsNetflix} (Expected: true)`);
      console.log(`Spotify alert logged? ${containsSpotify} (Expected: false)`);
      console.log(`Adobe alert logged? ${containsAdobe} (Expected: false)`);

      if (containsNetflix && !containsSpotify && !containsAdobe && histories.length === 2) {
        console.log('\n>>> SUCCESS: Alert engine behaves exactly as expected! <<<');
      } else {
        console.error('\n>>> FAILURE: Alert engine did not trigger correct logs! <<<');
      }
    } else {
      console.error('\n>>> FAILURE: alerts.log file was not created! <<<');
    }

  } catch (error) {
    console.error('Test execution failed with error:', error);
  } finally {
    // Close sqlite connection
    db.close((err) => {
      if (err) console.error('Error closing database:', err.message);
      else console.log('Database connection closed.');
    });
    console.log('--- VERIFICATION TEST FINISHED ---');
  }
};

runTest();
